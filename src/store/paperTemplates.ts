// 根据用户等级和科目获取模板
export const getTemplatesByLevelAndSubject = (level: string, subject: string): PaperTemplate[] => {
  return paperTemplates.filter(template => 
    template.level === level && 
    template.subject.toLowerCase().includes(subject.toLowerCase())
  );
};

// 根据ID获取模板
export const getTemplateById = (id: string): PaperTemplate | undefined => {
  return paperTemplates.find(template => template.id === id);
};

// 获取所有适合用户等级的模板
export const getTemplatesByLevel = (level: string): PaperTemplate[] => {
  return paperTemplates.filter(template => template.level === level);
};