export const getQuestionsBySubject = (subject: string): ExamQuestion[] => {
  return examQuestions.filter(question => 
    question.subject.toLowerCase().includes(subject.toLowerCase())
  );
};

// 根据ID获取真题
export const getQuestionById = (id: string): ExamQuestion | undefined => {
  return examQuestions.find(question => question.id === id);
};

// 根据年份获取真题
export const getQuestionsByYear = (year: string): ExamQuestion[] => {
  return examQuestions.filter(question => question.year === year);
};

// 根据类型获取真题
export const getQuestionsByType = (type: string): ExamQuestion[] => {
  return examQuestions.filter(question => question.type === type);
};

